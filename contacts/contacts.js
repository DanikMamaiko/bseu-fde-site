// Функция для перевода страницы
function translatePage(language) {
    // Устанавливаем атрибут lang для html
    document.documentElement.lang = language;

    // Обновляем title страницы
    document.title = translations[language]['contacts.title'] || 'Контакты';

    // Находим все элементы с атрибутом data-i18n и переводим их
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[language] && translations[language][key]) {
            element.innerHTML = translations[language][key];
        }
    });

    // Обновляем карту в зависимости от языка
    updateMapLanguage(language);
}



// Функция инициализации карты
function initMap() {
    // Проверяем, что ymaps загружен
    if (typeof ymaps === 'undefined') return;

    // Удаляем старую карту, если она существует
    const mapContainer = document.getElementById('map');
    while (mapContainer.firstChild) {
        mapContainer.removeChild(mapContainer.firstChild);
    }

    // Создаем новую карту
    const myMap = new ymaps.Map("map", {
        center: [53.878697, 27.606953],
        zoom: 15
    });

    const myPlacemark = new ymaps.Placemark([53.878697, 27.606953], {
        balloonContent: document.querySelector('[data-i18n="contacts.address"]').innerHTML
    });

    myMap.geoObjects.add(myPlacemark);
}

document.addEventListener('DOMContentLoaded', function() {
    // Инициализация карты при первой загрузке
    ymaps.ready(initMap);

    // Анимация собачки
    var dogElement = document.querySelector('.dog');
    if (dogElement) {
        dogElement.addEventListener('mouseenter', function() {
            dogElement.classList.add('rotate');
        });

        dogElement.addEventListener('mouseleave', function() {
            dogElement.classList.remove('rotate');
        });
    }

    // Переключение языка
    const languageItems = document.querySelectorAll('.language-dropdown-item');
    const languageDropdown = document.getElementById('languageDropdown');
    const desktopLanguageIcon = document.getElementById('desktopLanguageIcon');

    // Обработчик клика по иконке языка
    if (desktopLanguageIcon) {
        desktopLanguageIcon.addEventListener('click', function(e) {
            e.stopPropagation();
            languageDropdown.style.display = languageDropdown.style.display === 'block' ? 'none' : 'block';
        });
    }

    // Обработчики клика по пунктам меню языка
    languageItems.forEach(item => {
        item.addEventListener('click', function(e) {
            e.stopPropagation();
            const lang = this.getAttribute('data-lang');
            if (lang && translations[lang]) {
                // Удаляем класс active у всех пунктов
                languageItems.forEach(el => el.classList.remove('active'));
                // Добавляем класс active текущему пункту
                this.classList.add('active');
                // Переводим страницу
                translatePage(lang);
                // Закрываем выпадающее меню
                languageDropdown.style.display = 'none';
            }
        });
    });

    // Закрытие выпадающего меню при клике вне его
    document.addEventListener('click', function() {
        languageDropdown.style.display = 'none';
    });

    // По умолчанию загружаем русскую версию
    translatePage('ru');
});